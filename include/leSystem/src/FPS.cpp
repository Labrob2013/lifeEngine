#include "FPS.h"

//-------------------------------------------------------------------------//

le::FPS::FPS()
{
	iFrame = iFps = 0;
}

//-------------------------------------------------------------------------//

void le::FPS::UpdateFPS()
{
	if ( Clock.getElapsedTime().asSeconds() >= 1.f )
	{
		iFps = iFrame;
		iFrame = 0;
		Clock.restart();
	}

	++iFrame;
}

//-------------------------------------------------------------------------//

unsigned int le::FPS::GetFPS() const
{
	return iFps;
}

